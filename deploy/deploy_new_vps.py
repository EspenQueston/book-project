# -- required secrets (NO fallback defaults -- public repo!) ------------------
def _env(name: str) -> str:
    val = os.environ.get(name, "").strip()
    if not val:
        sys.exit(
            f"ERROR: required env var {name!r} is not set.\n"
            f"       Set it before running this script."
        )
    return val


SECRET_KEY          = _env("DJANGO_SECRET_KEY")

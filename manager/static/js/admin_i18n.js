        setLang: function (l) { lang = l; localStorage.setItem('adminLang', l); translatePage(); updateToggle(); },
        t: function (zh, en, fr) {
            if (lang === 'fr') return fr || en || zh;
            return lang === 'en' ? (en || zh) : zh;
        }
